<h2>Inherent Characteristics of Natural Fiber Flooring</h2>
<ul class="disc">
<li>When dealing with Natural Fiber Flooring, no guarantee can be made with certain characteristics such as color and weave. As such, one material may come out more perfect than another. Listed below are common irregularities a customer can expect.</li>
<li>A few plugs -the fibers have to be tied off in the manufacturing process.</li>
<li>Some irregularities in the ribs, some might be close, some will be further apart, especially in Jute.</li>
<li>Variation in bowing and loop size can occur.</li>
<li>All natural fibers tend to return to their original colors. Colors can fade over time.</li>
<li>Direct sunlight should be avoided to preserve color. Natural Floor Coverings are recommended for light to medium traffic environment and hold up well in household and commercial conditions. Situations where extreme humidity, heat, dampness (bathrooms, basements, and kitchens) or heavy traffic is expected, are to be avoided.</li>
<li>In offices, we recommend that loose mats be laid under chairs with castors to extend rug life.</li>
</ul>
