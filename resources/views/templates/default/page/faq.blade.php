<h2>What size should my rug be?</h2>
To a large degree, choosing the size of an area rug comes down to personal preference. You may be looking for a rug to underscore a coffee table, or you may be searching for something to put in the front entry. There are certain situations, though, that require you follow some guidelines. If you're shopping for a rug that will define a conversation grouping, start by putting your floor plan on paper. Then experiment with standard rug sizes.
The right rug will accomplish one of two things: It will either be large enough so that all of your seating pieces will fit comfortably on top of it, or it will be at least large enough for the front legs of each to fit. As a general rule, the sofa shouldn't be completely on the rug while any accompanying chairs should be big enough so that when a seated diner pushes back the chair, the back legs don't get caught in the process.
Area rugs are one of the easiest ways to delineate spaces in a large room. Two area rugs, matched in color and style but not in size, visually divide room into two distinct seating areas. Both rugs are large enough to comfortably accommodate their respective groupings.
