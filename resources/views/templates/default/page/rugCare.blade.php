    <h2>
        <b>How to Remove Sticky Latex from Hardwood Floors</b>
    </h2>
    <p>Hardwood floors can be damaged when sticky messes are removed improperly. The steps below will guide you through the process of safely removing sticky latex from hardwood floors. </p>
    <h3>
        <b>Step 1</b>
    </h3>
    <p>Apply WD-40 to the sticky latex. As you use it work in small sections.</p>
    <p>WD-40 is an excellent tool for removing sticky material from your wood floor because it won't dry on the wood. Even though that is true, work in small sections as this is the safest way to clean your floor.</p>
    <h3>
        <b>Step 2</b>
    </h3>
    <p>After applying the WD-40 allow up to 5 minutes, or even more if the latex is thick. During this time the WD-40 will break down the latex and interrupt the bond between it and the floor, allowing it to be removed.</p>
    <h3>
        <b>Step 3</b>
    </h3>
    <p>As the latex begins to loosen its grip begin wiping it away with a cloth scouring pad. If the scouring pad becomes gummed up with latex, change pads for more effective cleaning.</p>
    <h3>
        <b>Step 4</b>
    </h3>
    <p>Repeat the above steps until all of the latex is removed.</p>
    <h3>
        <b>Step 5</b>
    </h3>
    <p>There may be remaining residue from the WD-40, use a soft cloth to wipe that up. Be sure to wipe up any residual liquid as it will create a dangerous, slippery area.</p>
    <p>
        <i>Special Note: If you have unfinished wood floors, almost any attempt to clean the floor will cause staining. It is best to use fine grit sandpaper to remove a mess on unfinished wood. </i>
